/**
 * 헬퍼 함수: 카테고리와 등급을 전달받아 랜덤한 문구를 반환합니다.
 */
export const getRandomComment = (category, level) => {
    const pool = AI_STUDENT_COMMENTS[category][level];
    return pool[Math.floor(Math.random() * pool.length)];
};

/**
 * 종합 코멘트 생성 함수
 * @param {string} attLvl - 'high' | 'mid' | 'low'
 * @param {string} hwLvl - 'high' | 'mid' | 'low'
 * @param {string} prsLvl - 'high' | 'mid' | 'low'
 */
export const generateFullComment = (attLvl, hwLvl, prsLvl) => {
    const att = getRandomComment('attendance', attLvl);
    const hw = getRandomComment('homework', hwLvl);
    const prs = getRandomComment('praise', prsLvl);

    return `${att} ${hw} ${prs}`;
};

